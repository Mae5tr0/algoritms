    private final int n;
    private final int[][] blocks;
    private final int[][] tiles;

    // construct a board from an n-by-n array of blocks
    // (where blocks[i][j] = block in row i, column j)
    public Board(int[][] blocks) {
        n = blocks.length;
        this.blocks = blocks;

        tiles = new int[n][n];
    }

    // board dimension n
    public int dimension() {
        return n;
    }

